    <p><h4><b>Information</b></h4></p>
    <p style="text-align: justify; font-size: 18px;">Born 30.11.1991. in Teslic (RS / BiH) From 2011. to 2015. full-time student at the Academy of Serbian Orthodox Church for Arts and Conservation in Belgrade, majoring Church Art- Icon painting. Completed master studies in the same direction in 2018., under Professor Dr. Todor Mitrovic. Lives and works in Belgrade. Member of ULUPUDS.</p>

